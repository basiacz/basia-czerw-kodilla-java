package com.kodilla.patterns.strategy.social;

import org.junit.Assert;
import org.junit.Test;

public class UserTestSuite {
    @Test
    public void testDefaultPublisher() {
        //Given
        User john = new Millenials("John", "Smith");
        User kasia = new YGeneration("Kasia", "Czerw");
        User basia = new ZGeneration("Basia", "Czerw");
        //When
        String j = john.share();
        String k = kasia.share();
        String b = basia.share();
        //Then
        Assert.assertEquals("Info on Facebook", j);
        Assert.assertEquals("Info on Snapchat", k);
        Assert.assertEquals("Info on Twitter", b);
    }

    @Test
    public void testAnotherPublisher() {
        //Given
        User john = new Millenials("John", "Smith");
        User kasia = new YGeneration("Kasia", "Czerw");
        User basia = new ZGeneration("Basia", "Czerw");
        basia.setSocialPublisher(new FacebookPublisher());
        //When
        String j = john.share();
        String k = kasia.share();
        String b = basia.share();
        //Then
        Assert.assertEquals("Info on Facebook", j);
        Assert.assertEquals("Info on Snapchat", k);
        Assert.assertEquals("Info on Facebook", b);
    }
}